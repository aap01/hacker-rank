def pangrams(s):
    # Write your code here
    m = dict()
    count = 0
    for i in s:
        if i != " " and i.lower() not in m:
            m[i.lower()] = True
            count += 1
        if count >= 26:
            return 'pangram'
    return 'not pangram'


